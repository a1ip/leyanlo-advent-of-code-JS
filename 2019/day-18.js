const dirs = [
  [0, 1],
  [1, 0],
  [0, -1],
  [-1, 0],
];

const memo = {};

function getMinPath(map, currs, keys, doors) {
  // found all keys
  if (!Object.keys(keys).length) {
    return { minSteps: 0, minPathKeys: [] };
  }

  const queue = dirs
    .map((dir) =>
      currs.map((curr) => ({
        coords: [
          [curr[0] + dir[0], curr[1] + dir[1]],
          ...currs.filter((c) => c.join() !== curr.join()),
        ],
        steps: 1,
        pathKeys: [],
      }))
    )
    .flat();
  const seen = currs.reduce((seen, curr) => {
    seen[curr.join()] = true;
    return seen;
  }, {});
  let minSteps = 0;
  let minPathKeys = [];
  while (queue.length) {
    const {
      coords: [[row, col], ...restCoords],
      steps,
      pathKeys,
    } = queue.shift();
    const char = map[row][col];
    if (seen[[row, col].join()]) {
      // skip if seen
      continue;
    }
    seen[[row, col].join()] = true;

    if (char === '#' || /[A-Z]/.test(char)) {
      // skip walls and doors
    } else if (char === '.') {
      queue.push(
        ...dirs.map((dir) => ({
          coords: [[row + dir[0], col + dir[1]], ...restCoords],
          steps: steps + 1,
          pathKeys: [...pathKeys],
        }))
      );
    } else if (/[a-z]/.test(char)) {
      const nextRows = map.map((row) => [...row]);
      nextRows[row][col] = '.';
      const door = doors[char.toUpperCase()];
      if (door) {
        // unlock door if present
        nextRows[door[0]][door[1]] = '.';
      }
      const nextStart = [[row, col], ...restCoords];
      const nextKeys = { ...keys };
      delete nextKeys[char];
      const nextDoors = { ...doors };
      delete nextDoors[char.toUpperCase()];
      const memoKey = [...nextStart, ...Object.keys(nextKeys)].join();
      const minPath =
        memo[memoKey] || getMinPath(nextRows, nextStart, nextKeys, nextDoors);
      memo[memoKey] = minPath;
      if (minSteps === 0 || steps + minPath.minSteps < minSteps) {
        minSteps = steps + minPath.minSteps;
        minPathKeys = [char, ...minPath.minPathKeys];
      }
    }
  }
  return { minSteps, minPathKeys };
}

function solve1(input) {
  const map = input.split('\n').map((row) => row.split(''));
  let currs;
  const keys = {};
  const doors = {};
  for (let i = 0; i < map.length; i++) {
    for (let j = 0; j < map[i].length; j++) {
      if (map[i][j] === '@') {
        // remove @ and keep track of curr separately
        map[i][j] = '.';
        currs = [[i, j]];
      } else if (/[a-z]/.test(map[i][j])) {
        keys[map[i][j]] = [i, j];
      } else if (/[A-Z]/.test(map[i][j])) {
        doors[map[i][j]] = [i, j];
      }
    }
  }

  console.log(getMinPath(map, currs, keys, doors).minSteps);
}

function solve2(input) {
  const map = input.split('\n').map((row) => row.split(''));
  let currs;
  const keys = {};
  const doors = {};
  for (let i = 0; i < map.length; i++) {
    for (let j = 0; j < map[i].length; j++) {
      if (map[i][j] === '@') {
        for (let k = i - 1; k <= i + 1; k++) {
          for (let l = j - 1; l <= j + 1; l++) {
            map[k][l] = k === i || l === j ? '#' : '.';
          }
        }
        // remove @ and keep track of curr separately
        map[i][j] = '.';
        currs = [
          [i - 1, j - 1],
          [i - 1, j + 1],
          [i + 1, j - 1],
          [i + 1, j + 1],
        ];
      } else if (/[a-z]/.test(map[i][j])) {
        keys[map[i][j]] = [i, j];
      } else if (/[A-Z]/.test(map[i][j])) {
        doors[map[i][j]] = [i, j];
      }
    }
  }

  console.log(getMinPath(map, currs, keys, doors).minSteps);
}

