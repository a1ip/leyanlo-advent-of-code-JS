function process1(input) {
  let nTrees = 0;
  let j = -3;
  for (let i = 0; i < input.length; i++) {
    j += 3;
    if (input[i][j % 31] === "#") {
      nTrees++;
    }
  }
  console.log(nTrees);
}

function process(input) {
  const product = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]].reduce(
    (product, [right, down]) => {
      let nTrees = 0;
      let j = -right;
      for (let i = 0; i < input.length; i += down) {
        j += right;
        if (input[i][j % 31] === "#") {
          nTrees++;
        }
      }
      return product * nTrees;
    },
    1
  );
  console.log(product);
}

